#include "corewar.h"

int		mem_rev(int mem)
{
	return ((mem & 0x000000FF) << 24
	| (mem & 0x0000FF00) << 8
	| (mem & 0x00FF0000) >> 8
	| (mem & 0xFF000000) >> 24);
}
